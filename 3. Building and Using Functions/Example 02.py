
# Arguments

def greet(lang):
    if lang == "es":
        print("Hola")
    elif lang == "fr":
        print("Bonjour")
    else:
        print("Hello")

# Calling the function and passing in the argument "en":
greet("en")

# Calling the function and passing in the argument "es":
greet("es")

# And finally calling the function and passing in the argument "fr":
greet("fr")

